    private void ButUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButUpdateActionPerformed
        Book book = new Book();
        
        book.setBookId(txtBookId.getText().trim());
        book.setTitle(txtBookTitle.getText().trim());
        book.setAuthor(txtAuthor.getText().trim());
        book.setPublisherName(txtPublisher.getText().trim());
        book.setDescription(txtDescription.getText().trim());
        book.setNoOfPage(Integer.parseInt(txtNoOfPage.getText().trim()));
        book.setCategory(ComboCattegory.getSelectedItem().toString());
        book.setUserEntres(txtUserEntres.getText().trim());
        
        BookManager bookManager = new BookManager();
        String msg;
        try {
            msg = bookManager.ModifyBook(book);
            lblMassage.setText("Save Successful");
        } catch (SQLException ex) {
            Logger.getLogger(GUIModifyBook.class.getName()).log(Level.SEVERE, null, ex);
            lblMassage.setText("Unsuccessful");
        }
        

    }//GEN-LAST:event_ButUpdateActionPerformed

