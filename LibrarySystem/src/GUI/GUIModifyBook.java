    private void txtBookIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBookIdKeyPressed
        lblMassage.setText("");
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                BookManager bookManager = new BookManager();
                Book book = bookManager.SearchBookId(txtBookId.getText().trim());
                
                txtBookId.setText(book.getBookId().trim());
                txtAuthor.setText(book.getAuthor().trim());
                txtBookTitle.setText(book.getTitle().trim());
                txtDescription.setText(book.getDescription().trim());
                txtNoOfPage.setText(book.getNoOfPage() + "".trim());
                txtPublisher.setText(book.getPublisherName().trim());
                txtUserEntres.setText(book.getUserEntres().trim());
                ComboCattegory.setSelectedItem(book.getCategory().trim());
            } catch (Exception ex) {
                Logger.getLogger(GUIModifyBook.class.getName()).log(Level.SEVERE, null, ex);
                lblMassage.setText("Invalid ID");
            }
            
        }
    }//GEN-LAST:event_txtBookIdKeyPressed

