    private void ButSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButSaveActionPerformed

        Member member = new Member();

        member.setMemberId(txtMemberId.getText().trim());
        member.setMemberName(txtMemberName.getText().trim());
        member.setDOB(txtDOB.getText().trim());
        member.setPhone(txtPhone.getText().trim());
        member.setEmail(txtEmail.getText().trim());
        member.setAddress(txtAddress.getText().trim());

        MemberMnager memberManager = new MemberMnager();
        String msg;
        try {
            msg = memberManager.ModifyMember(member);
            lblMassage.setText("Save Successful");
        } catch (SQLException ex) {
            lblMassage.setText("Unsuccessful");
            Logger.getLogger(GUIModifyMember.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_ButSaveActionPerformed

