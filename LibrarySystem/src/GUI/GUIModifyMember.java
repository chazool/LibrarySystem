    private void txtMemberIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMemberIdKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            MemberMnager memberManager = new MemberMnager();
            Member member = memberManager.SearchMember((txtMemberId.getText().trim()));

            txtAddress.setText(member.getAddress());
            txtDOB.setText(member.getDOB());
            txtEmail.setText(member.getEmail());
            txtMemberName.setText(member.getMemberName());
            txtPhone.setText(member.getPhone());
        }
    }//GEN-LAST:event_txtMemberIdKeyPressed

